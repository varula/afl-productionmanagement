// Form field configurations for each MIS document type
// Each document type has unique fields stored as JSONB in the `data` column

export interface FormField {
  key: string;
  label: string;
  type: 'text' | 'textarea' | 'number' | 'date' | 'select' | 'checkbox' | 'percentage';
  placeholder?: string;
  options?: { label: string; value: string }[];
  required?: boolean;
  defaultValue?: any;
}

export interface DocumentTypeConfig {
  documentType: string;
  number: number;
  title: string;
  linkTo: 'style' | 'date' | 'line' | 'style_line' | 'none';
  fields: FormField[];
}

